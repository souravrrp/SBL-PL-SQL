 WHERE     1 = 1
       AND (:P_LOOKUP_TYPE IS NULL OR (UPPER (LOOKUP_TYPE) LIKE UPPER ('%' || :P_LOOKUP_TYPE || '%')))
       AND (:P_LOOKUP_CODE IS NULL OR (UPPER (LOOKUP_CODE) LIKE UPPER ('%' || :P_LOOKUP_CODE || '%')))
       AND (:P_MEANING IS NULL OR (UPPER (MEANING) LIKE UPPER ('%' || :P_MEANING || '%')))
       AND (:P_DESCRIPTION IS NULL OR (UPPER (DESCRIPTION) LIKE UPPER ('%' || :P_DESCRIPTION || '%')))
