-- Picking line status from WSH_DELIVERY_DETAILS table

SELECT FND.LOOKUP_CODE,
       FND.MEANING
  FROM APPS.FND_LOOKUP_VALUES FND
 WHERE FND.LOOKUP_TYPE='PICK_STATUS'
   AND LANGUAGE ='US'
 ORDER BY lookup_code;

--  Delivery FIRM Status

SELECT FLV.LOOKUP_CODE,
       FLV.MEANING
  FROM APPS.FND_LOOKUP_VALUES FLV
 WHERE FLV.LANGUAGE = 'US'
   AND FLV.LOOKUP_TYPE = 'DELIVERY_PLANNED_FLAG'
 ORDER BY lookup_code;

-- Delivery Status

SELECT FLV.LOOKUP_CODE, 
       FLV.MEANING
  FROM APPS.FND_LOOKUP_VALUES FLV 
 WHERE FLV.LANGUAGE = 'US'
   and flv.lookup_type = 'DELIVERY_STATUS'
 ORDER BY lookup_code;
  
-- WMS TASK status

SELECT *
  FROM APPS.MFG_LOOKUPS
 WHERE LOOKUP_TYPE = 'WMS_TASK_STATUS'
 ORDER BY lookup_code;

-- WMS Task Type

SELECT *
  FROM APPS.MFG_LOOKUPS
 WHERE LOOKUP_TYPE = 'WMS_TASK_TYPES'
 ORDER BY lookup_code;

-- WMS LPN  Context

SELECT *
  FROM APPS.MFG_LOOKUPS
 WHERE LOOKUP_TYPE = 'WMS_LPN_CONTEXT'
 ORDER BY LOOKUP_CODE;

-- Serial Number status

select *
  FROM APPS.MFG_LOOKUPS
 WHERE LOOKUP_TYPE = 'SERIAL_NUM_STATUS'
 order by lookup_code;

-- WIP Job status

