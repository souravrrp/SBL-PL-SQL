  FROM APPS.MFG_LOOKUPS
 where lookup_type like 'MTL_TRANSACTION_ACTION';

-- INV transaction Source Type

SELECT *
  FROM APPS.MFG_LOOKUPS
 WHERE lookup_type like 'MTL_TRANSACTION_SOURCE';

