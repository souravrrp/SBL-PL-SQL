SELECT * FROM APPS.MFG_LOOKUPS WHERE LOOKUP_TYPE = 'WIP_SUPPLY'

SELECT * FROM APPS.MFG_LOOKUPS WHERE LOOKUP_TYPE = 'BOM_ITEM_TYPE'

--  Move Order Status

